#include <unistd.h>

void	plus_1(char *a)
{
	if (a[1] < '9')
	{
		a[1]++;
	}
	else
	{
		a[1] = '0';
		a[0]++;
	}
}

void	ft_print_comb2(void)
{
	char a[3];
	char b[2];

	a[0] = '0';
	a[1] = '0';
	a[2] = ' ';
	b[0] = '0';
	b[1] = '0';
	while (a[0] <= '9' && a[1] <= '9')
	{
		while (b[0] <= '9' && b[1] <= '9')
		{
			write(1, a, 3);
			write(1, b, 2);
			if (a[0] != '9' | a[1] != '8' | b[0] != '9' | b[1] != '9')
				write(1, ", ", 2);
			plus_1(b);
		}
		plus_1(a);
		b[0] = a[0];
		b[1] = a[1] + 1;
	}
}
