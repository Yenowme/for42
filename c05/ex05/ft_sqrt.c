int	ft_sqrt(int nb)
{
	int rt;
	int i;

	i = 1;
	rt = 0;
	if (nb < 0)
		return (0);
	while (rt < nb)
	{
		rt = i * i;
		if (rt == nb)
			return (i);
		i++;
	}
	return (0);
}
